/**
 * 根据 phase 获取阶段定义
 */
export function getStage(phase) {
  return STAGES.find(s => s.id === phase) || null;
}

/**
 * 获取阶段标签（用于前端进度条）
 */
export function getStageLabel(phase) {
  const stage = getStage(phase);
  return stage ? stage.label : '';
}
